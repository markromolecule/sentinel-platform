import {
    type DocumentKind,
    type HeaderConfig,
    type FooterConfig,
    HeaderConfigSchema,
    FooterConfigSchema
} from '@sentinel/shared/types';

export interface ResolvedPdfTemplate {
    templateId: string | null;
    documentKind: DocumentKind;
    headerConfig: HeaderConfig;
    footerConfig: FooterConfig;
}

