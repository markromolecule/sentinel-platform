/**
 * Resolves the active PDF template for a given institution and document kind, applying the precedence rules:
 * 1. Institution-published override.
 * 2. Global-published template.
 * 3. Code-owned built-in default.
 * 
 * @param dbClient database client
 * @param institutionId institution ID
 * @param documentKind document kind
 * @returns resolved template details
 */
export async function resolvePdfTemplate(
    dbClient: DbClient,
    institutionId: string | null,
    documentKind: DocumentKind
): Promise<ResolvedPdfTemplate> {
    // 1. Try to find institution-published override
    if (institutionId) {
        const instOverride = await dbClient
            .selectFrom('pdf_templates')
            .selectAll()
            .where('institution_id', '=', institutionId)
            .where('document_kind', '=', documentKind)
            .where('status', '=', 'PUBLISHED')
            .executeTakeFirst();

        if (instOverride) {
            return {
                templateId: instOverride.template_id,
                documentKind,
                headerConfig: HeaderConfigSchema.parse(instOverride.header_config),
                footerConfig: FooterConfigSchema.parse(instOverride.footer_config),
            };
        }
    }

    // 2. Try to find global-published template
    const globalTemplate = await dbClient
        .selectFrom('pdf_templates')
        .selectAll()
        .where('institution_id', 'is', null)
        .where('document_kind', '=', documentKind)
        .where('status', '=', 'PUBLISHED')
        .executeTakeFirst();

    if (globalTemplate) {
        return {
            templateId: globalTemplate.template_id,
            documentKind,
            headerConfig: HeaderConfigSchema.parse(globalTemplate.header_config),
            footerConfig: FooterConfigSchema.parse(globalTemplate.footer_config),
        };
    }

    // 3. Fall back to built-in defaults
    const builtIn = BUILT_IN_DEFAULTS[documentKind];
    return {
        templateId: null,
        documentKind,
        headerConfig: builtIn.headerConfig,
        footerConfig: builtIn.footerConfig,
    };
}
